function sqlLiteral(str) {
  return "'" + String(str).replace(/'/g, "''") + "'"
}

function defaultPricingTypeForCategory(catSlug) {
  if (QUOTE_DEFAULT_CATEGORY_SLUGS.has(catSlug)) return 'quote'
  if (HOURLY_DEFAULT_CATEGORY_SLUGS.has(catSlug)) return 'hourly'
  return 'fixed'
}

function buildTaxonomySeedSql(cats) {
  const lines = [
    '-- Seed categories + subcategories from data/markedsplass-tjenester.csv',
    '-- Generated by: node scripts/generate-taxonomy.mjs',
    '-- Idempotent (ON CONFLICT); safe to re-run after CSV/taxonomy changes.',
    '',
  ]

  cats.forEach((cat, idx) => {
    const order = idx * 10
    lines.push(
      `INSERT INTO public.categories (name, slug, sort_order) VALUES (${sqlLiteral(cat.title)}, ${sqlLiteral(cat.slug)}, ${order})`,
      `ON CONFLICT (slug) DO UPDATE SET name = EXCLUDED.name, sort_order = EXCLUDED.sort_order;`,
      '',
    )

    const pricing = defaultPricingTypeForCategory(cat.slug)
    const valueRows = cat.subs.map(
      (s) =>
        `  (${sqlLiteral(s.title)}, ${sqlLiteral(s.slug)}, ${sqlLiteral(pricing)})`,
    )
    lines.push(
      `INSERT INTO public.subcategories (category_id, name, slug, default_pricing_type)`,
      `SELECT c.id, v.name, v.slug, v.pricing::public.pricing_type`,
      `FROM public.categories c`,
      `CROSS JOIN (VALUES`,
      valueRows.join(',\n') + ') AS v(name, slug, pricing)',
      `WHERE c.slug = ${sqlLiteral(cat.slug)}`,
      `ON CONFLICT (category_id, slug) DO UPDATE SET`,
      `  name = EXCLUDED.name,`,
      `  default_pricing_type = EXCLUDED.default_pricing_type;`,
      '',
    )
  })

  return lines.join('\n')
}

function parseCsvLine(line) {
  const out = []
  let cur = ''
  let inQ = false
  for (let i = 0; i < line.length; i++) {
    const c = line[i]
    if (inQ) {
      if (c === '"') {
        if (line[i + 1] === '"') {
          cur += '"'
          i++
        } else {
          inQ = false
        }
      } else {
        cur += c
      }
    } else if (c === '"') {
      inQ = true
    } else if (c === ',') {
      out.push(cur)
      cur = ''
    } else {
      cur += c
    }
  }
  out.push(cur)
  return out
}

/** Norwegian-friendly URL slug */
function slugify(s) {
  return s
    .toLowerCase()
    .trim()
    .replace(/æ/g, 'ae')
    .replace(/ø/g, 'o')
    .replace(/å/g, 'a')
    .replace(/ü/g, 'u')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '') || 'tjeneste'
}

