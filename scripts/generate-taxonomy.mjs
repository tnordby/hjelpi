function canonicalCategoryTitle(rawService) {
  return SERVICE_TO_CANONICAL[rawService] ?? rawService
}

const text = fs.readFileSync(csvPath, 'utf8')
const lines = text.split(/\r?\n/).filter((l) => l.trim())

const rows = []
for (let i = 1; i < lines.length; i++) {
  const [service, sub] = parseCsvLine(lines[i])
  if (!service?.trim() || !sub?.trim()) continue
  rows.push({ service: service.trim(), sub: sub.trim() })
}

/** categoryTitle -> Map<subSlug, title> */
const buckets = new Map()

for (const { service, sub } of rows) {
  const catTitle = canonicalCategoryTitle(service)
  const fixes = LABEL_FIXES[service] ?? LABEL_FIXES[catTitle] ?? {}
  const title = fixes[sub] ?? sub
  const catSlug =
    CATEGORY_SLUG_OVERRIDES[catTitle] ?? slugify(catTitle)
  const subSlug = slugify(title)
  const skipKey = `${catSlug}|${subSlug}`
  if (SKIP_SUBS.has(skipKey)) continue

  if (!buckets.has(catTitle)) buckets.set(catTitle, new Map())
  const m = buckets.get(catTitle)
  if (!m.has(subSlug)) m.set(subSlug, title)
  else {
    const existing = m.get(subSlug)
    if (title.length > existing.length) m.set(subSlug, title)
  }
}

const categories = [...buckets.entries()]
  .map(([title, subMap]) => {
    const slug = CATEGORY_SLUG_OVERRIDES[title] ?? slugify(title)
    const subs = [...subMap.entries()]
      .map(([slugSub, subTitle]) => ({ slug: slugSub, title: subTitle }))
      .sort((a, b) => a.title.localeCompare(b.title, 'nb'))
    return { slug, title, subs }
  })
  .sort((a, b) => a.title.localeCompare(b.title, 'nb'))

